cables = [4, 3, 2, 6]
print("–ú—ñ–Ω—ñ–º–∞–ª—å–Ω—ñ –∑–∞–≥–∞–ª—å–Ω—ñ –≤–∏—Ç—Ä–∞—Ç–∏:", min_connection_cost(cables))

