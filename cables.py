import heapq

def min_connection_cost(cables):
    # –°—Ç–≤–æ—Ä—é—î–º–æ –º—ñ–Ω-–∫—É–ø—É –∑ –¥–æ–≤–∂–∏–Ω –∫–∞–±–µ–ª—ñ–≤
    heapq.heapify(cables)
    total_cost = 0

    # –ü–æ–≤—Ç–æ—Ä—é—î–º–æ, –ø–æ–∫–∏ –Ω–µ –∑–∞–ª–∏—à–∏—Ç—å—Å—è –æ–¥–∏–Ω –∫–∞–±–µ–ª—å
    while len(cables) > 1:
        # –î—ñ—Å—Ç–∞—î–º–æ –¥–≤–∞ –Ω–∞–π–∫–æ—Ä–æ—Ç—à–∏—Ö –∫–∞–±–µ–ª—ñ
        first = heapq.heappop(cables)
        second = heapq.heappop(cables)

        # –í–∏—Ç—Ä–∞—Ç–∏ –Ω–∞ —ó—Ö –æ–±‚Äô—î–¥–Ω–∞–Ω–Ω—è
        cost = first + second
        total_cost += cost

        # –î–æ–¥–∞—î–º–æ –Ω–æ–≤–∏–π –∫–∞–±–µ–ª—å –Ω–∞–∑–∞–¥ —É –∫—É–ø—É
        heapq.heappush(cables, cost)

    return total_cost

